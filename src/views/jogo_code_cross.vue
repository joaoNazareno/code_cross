    movimentarJogador(event) {
      switch (event.key) {
        case 'ArrowUp':
          if (this.mapa[this.jogador.y - 1][this.jogador.x] !== 0) {
            this.jogador.y--;
          }
          break;
        case 'ArrowDown':
          if (this.mapa[this.jogador.y + 1][this.jogador.x] !== 0) {
            this.jogador.y++;
          }
          break;
        case 'ArrowLeft':
          if (this.mapa[this.jogador.y][this.jogador.x - 1] !== 0) {
            this.jogador.x--;
          }
          break;
        case 'ArrowRight':
          if (this.mapa[this.jogador.y][this.jogador.x + 1] !== 0) {
            this.jogador.x++;
          }
          break;
      }
      this.checarColisao();
    },

    checarColisao() {
      const tile = this.mapa[this.jogador.y][this.jogador.x];
      if (tile === 2) { 
        this.exibirGameOver();
      } else if (tile === 4) {
        this.vitoria();
      } else {
        this.atualizarTela();
      }
    },

    atualizarTela() {
      this.desenharMapa();
      this.desenharJogador();
    },

    iniciarGravidade() {
      setInterval(() => {
        if (this.mapa[this.jogador.y + 1][this.jogador.x] === 1) {
          this.jogador.y++;
        }
        this.atualizarTela();
      }, 100);
    }
