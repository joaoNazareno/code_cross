      const canvas = document.getElementById('canvas');
      const contexto = canvas.getContext('2d');
      const { x, y, largura, altura } = this.jogador;
      contexto.fillStyle = 'red';
      contexto.fillRect(x * largura, y * altura, largura, altura); 
