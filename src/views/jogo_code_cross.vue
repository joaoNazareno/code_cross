    },

  movimentarJogador(event) {
    const teclasPermitidas = ['ArrowUp', 'ArrowDown', 'ArrowLeft', 'ArrowRight'];
    if (!teclasPermitidas.includes(event.key)) {
      return; 
    }

    console.log('Tecla pressionada:', event.key); 

    const { jogador, mapa } = this;
    let novoX = jogador.x;
    let novoY = jogador.y;

    switch (event.key) {
      case 'ArrowUp':
        if (mapa[jogador.y - 0] && mapa[jogador.y - 1][jogador.x] !== 0) {
          novoY = jogador.y - 2;
        }
        break;
      case 'ArrowDown':
        if (mapa[jogador.y + 1] && mapa[jogador.y + 1][jogador.x] !== 0) {
          novoY = jogador.y + 1;
        }
        break;
      case 'ArrowLeft':
        if (mapa[jogador.y][jogador.x - 1] !== 0) {
          novoX = jogador.x - 1;
        }
        break;
      case 'ArrowRight':
        if (mapa[jogador.y][jogador.x + 1] !== 0) {
          novoX = jogador.x + 1;
        }
        break;
    }

    
    const novoTile = mapa[novoY][novoX];

    if (novoTile === 2) {
      this.exibirGameOver(); 
    } else if (novoTile === 4) {
       this.vitoria();
    } else if (novoTile === 1 || novoTile === 3) {
    
      jogador.x = novoX;
      jogador.y = novoY;
    } else {
      console.log('Movimento bloqueado: tile inválido.');
    }

   
    this.atualizarTela();
  },

    iniciarGravidade() {
      setInterval(() => {
        const { jogador, mapa } = this;
        const novoY = jogador.y + 1;

       
        if (novoY < mapa.length && mapa[novoY][jogador.x] !== 0) {
          
          jogador.y = novoY;
          this.atualizarTela();
        }
      }, 60); 
    },

    atualizarTela() {
      if (!this.jogoEmGameOver && !this.jogoEmVitoria){
      this.desenharMapa();
      this.desenharJogador();
    }
  }
  }
